const now = new Date()

function hoursAgo(h: number) {
  return new Date(now.getTime() - h * 3600000).toISOString()
}
function hoursFromNow(h: number) {
  return new Date(now.getTime() + h * 3600000).toISOString()
}

function generateTelemetry(
  count: number,
  baseTemp: number,
  spike: boolean,
  startLat: number,
  startLng: number,
  endLat: number,
  endLng: number
): TelemetryReading[] {
  const readings: TelemetryReading[] = []
  for (let i = 0; i < count; i++) {
    const progress = i / (count - 1)
    const tempNoise = (Math.random() - 0.5) * 1.5
    const tempSpike = spike && i > count * 0.6 && i < count * 0.75 ? 4.5 : 0
    readings.push({
      timestamp: new Date(now.getTime() - (count - i) * 1800000).toISOString(),
      temperature: +(baseTemp + tempNoise + tempSpike).toFixed(1),
      humidity: +(45 + (Math.random() - 0.5) * 10).toFixed(1),
      shock: +(Math.random() * (spike && i === Math.floor(count * 0.65) ? 3.2 : 0.4)).toFixed(2),
      lat: startLat + (endLat - startLat) * progress + (Math.random() - 0.5) * 0.5,
      lng: startLng + (endLng - startLng) * progress + (Math.random() - 0.5) * 0.5,
      batteryLevel: +(95 - progress * 20 + (Math.random() - 0.5) * 3).toFixed(0),
      signalStrength: +(80 + (Math.random() - 0.5) * 20).toFixed(0),
    })
  }
  return readings
}

